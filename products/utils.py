"""
Утилиты для products app
"""
from django.utils.text import slugify as django_slugify


def slugify(text):
    """
    Создает slug из кириллического текста.

    Django's default slugify() удаляет кириллические символы, что приводит к пустым slug.
    Эта функция транслитерирует кириллицу в латиницу перед slugify.

    Примеры:
        slugify("Одежда") -> "odezhda"
        slugify("Продукты питания") -> "produkty-pitaniya"
        slugify("123 Test Тест") -> "123-test-test"
    """
    # Таблица транслитерации кириллицы в латиницу (ГОСТ 7.79-2000)
    transliteration_map = {
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd',
        'е': 'e', 'ё': 'yo', 'ж': 'zh', 'з': 'z', 'и': 'i',
        'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n',
        'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
        'у': 'u', 'ф': 'f', 'х': 'kh', 'ц': 'ts', 'ч': 'ch',
        'ш': 'sh', 'щ': 'shch', 'ъ': '', 'ы': 'y', 'ь': '',
        'э': 'e', 'ю': 'yu', 'я': 'ya',
        # Прописные буквы
        'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D',
        'Е': 'E', 'Ё': 'Yo', 'Ж': 'Zh', 'З': 'Z', 'И': 'I',
        'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M', 'Н': 'N',
        'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T',
        'У': 'U', 'Ф': 'F', 'Х': 'Kh', 'Ц': 'Ts', 'Ч': 'Ch',
        'Ш': 'Sh', 'Щ': 'Shch', 'Ъ': '', 'Ы': 'Y', 'Ь': '',
        'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya',
    }

    # Транслитерируем текст
    result = []
    for char in text:
        result.append(transliteration_map.get(char, char))

    # Применяем стандартный slugify к транслитерированному тексту
    return django_slugify(''.join(result))
